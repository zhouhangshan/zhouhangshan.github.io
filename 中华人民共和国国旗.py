import turtle
turtle.setup(800,600,10,10)
tt = turtle.Turtle()
tt.pensize(1)
tt.speed(10)
tt.pencolor('red')
tt.fillcolor('red')
tt.penup()
tt.goto(-300,-200)
tt.pendown()
tt.begin_fill()
tt.goto(300,-200)
tt.goto(300,200)
tt.goto(-300,200)
tt.goto(-300,-200)
tt.end_fill()
tt.penup()
tt.pencolor('yellow')
tt.fillcolor('yellow')
tt.goto(-260,130)
tt.pendown()
tt.begin_fill()
for i in range(0,5):
    tt.forward(40)
    tt.left(72)
    tt.forward(40)
    tt.right(144)
tt.end_fill()
tt.penup()
tt.goto(-120,170)
tt.pendown()
tt.begin_fill()
tt.right(20)
for i in range(0,5):
    tt.forward(10)
    tt.left(72)
    tt.forward(10)
    tt.right(144)
tt.end_fill()
tt.penup()
tt.goto(-90,130)
tt.left(30)
tt.pendown()
tt.begin_fill()
for i in range(0,5):
    tt.forward(10)
    tt.left(72)
    tt.forward(10)
    tt.right(144)
tt.end_fill()
tt.penup()
tt.goto(-94,90)
tt.right(10)
tt.pendown()
tt.begin_fill()
for i in range(0,5):
    tt.forward(10)
    tt.left(72)
    tt.forward(10)
    tt.right(144)
tt.end_fill()
tt.penup()
tt.goto(-120,60)
tt.right(15)
tt.pendown()
tt.begin_fill()
for i in range(0,5):
    tt.forward(10)
    tt.left(72)
    tt.forward(10)
    tt.right(144)
tt.end_fill()
tt.penup()
tt.goto(400,400)
turtle.done()
